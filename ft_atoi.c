int		ft_atoi(const char *str)
{
	int		neg;
	int		num;
	int		i;

	neg = 1;
	i = 0;
	if (str[0] == '-')
	{
		neg = -1;
		i++;
	}
	num = 0;
	while (str[i])
	{
		num = num * 10 + str[i] - '0';
		i++;
	}
	return (neg * num);
}
