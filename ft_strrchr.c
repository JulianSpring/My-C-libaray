#include "libft.h"

char	*ft_strrchr(char *str, int c)
{
	int		i;
	int		a;
	char	*b;

	i = 0;
	a = 0;
	while (str[i] != '\0')
	{
		if (str[i] == (char)c)
		{
			b = &str[i];
			a = 1;
		}
		i++;
	}
	if (a == 0)
		return (NULL);
	return (b);
}
